Crazy Lou (died 1886) was the pseudonym of a New York showgirl and prostitute who was a well-known personality in the Bowery during the late 19th century. Described as a "famous local harlot derived from Boston society",[1] little of her life is known prior to her arrival in New York. She is said to have been the daughter of a wealthy Boston merchant who, at the age of 17, was seduced by a young man and travelled to New York to join him. She instead fell into the hands of procuresses who sold her to one of the Seven Sisters brothels, a group of seven well-known establishments located in a residential area of brownstones on West 25th Street. Forced into prostitution, she was eventually released "when her beauty faded".[2][3]

